def generate_map(slide_num, lat, lon, label, extent):
    outpath = f'{OUT_DIR}/slide_{slide_num}_img_1.png'
    if os.path.exists(outpath):
        return False

    fig, ax = plt.subplots(figsize=(5, 4), subplot_kw={'projection': ccrs.PlateCarree()})
    ax.set_extent(extent)
    ax.add_feature(cfeature.LAND, facecolor='#2a2a2a')
    ax.add_feature(cfeature.OCEAN, facecolor='#1a1a1a')
    ax.add_feature(cfeature.BORDERS, linewidth=0.3, edgecolor='#444')
    ax.add_feature(cfeature.COASTLINE, linewidth=0.5, edgecolor='#555')
    ax.set_facecolor('#1a1a1a')

    # Concentric circle marker
    ax.plot(lon, lat, marker='o', color='#c9a84c', markersize=10,
            markeredgecolor='#c9a84c', markeredgewidth=2, markerfacecolor='none',
            transform=ccrs.PlateCarree())
    ax.plot(lon, lat, marker='o', color='#c9a84c', markersize=4,
            transform=ccrs.PlateCarree())

    # Label
    text_offset_x = (extent[1] - extent[0]) * 0.04
    ax.text(lon + text_offset_x, lat, label, color='#c9a84c',
            fontsize=9, fontfamily='serif', fontstyle='normal',
            transform=ccrs.PlateCarree(),
            verticalalignment='center')

    fig.patch.set_facecolor('#1a1a1a')
    plt.savefig(outpath, dpi=100, bbox_inches='tight', pad_inches=0.1, facecolor='#1a1a1a')
    plt.close()
    return True

if __name__ == '__main__':
    generated = 0
    skipped = 0
    for slide_num, (lat, lon, label, extent) in sorted(LOCATIONS.items()):
        if generate_map(slide_num, lat, lon, label, extent):
            generated += 1
        else:
            skipped += 1
        if generated % 20 == 0 and generated > 0:
            print(f'  Generated {generated} maps so far...')
    print(f'Done! Generated {generated} new maps, skipped {skipped} existing.')
